  private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
      // TODO add your handling code here:
      try {
          String username = txtUsername.getText();
          String password = txtPassword.getText().toString();
          String domain = txtDomain.getText();
          if (!IsValid.IPAndDomain(domain)) {
              JOptionPane.showMessageDialog(
                      rootPane,
                      "Your domain/ip is invalied!!! Enter again");
          } else {
              socket = new Socket(domain, StringUtils.PORT);
              Request r = new Request(StringUtils.LOGIN, new LoginModel(username, password));
              Gson gson = new Gson();
              DataInputStream in = new DataInputStream(socket.getInputStream());
              DataOutputStream out = new DataOutputStream(socket.getOutputStream());
              String s = gson.toJson(r);
              out.writeUTF(s);
              s = in.readUTF();
              if (s.equals(StringUtils.LOGINOK)) {
                  JOptionPane.showMessageDialog(rootPane, "Connect to server success");
                  frmMain main = new frmMain(socket, username);
                  main.setVisible(true);
                  this.dispose();
              }
          }
      } catch (Exception e) {
          JOptionPane.showMessageDialog(rootPane, "Khong the ket noi den may chu");
      }
  }//GEN-LAST:event_btnLoginActionPerformed

